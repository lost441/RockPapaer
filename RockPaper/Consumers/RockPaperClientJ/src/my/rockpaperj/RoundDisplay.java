package my.rockpaperj;

/**
 *
 * @author kmarais
 */
public class RoundDisplay {
    private int round;
    private String myHand;
    private String opponentHand;
    private String outcome;

    public int getRound() {
        return round;
    }

    public void setRound(int round) {
        this.round = round;
    }

    public String getMyHand() {
        return myHand;
    }

    public void setMyHand(String myHand) {
        this.myHand = myHand;
    }

    public String getOpponentHand() {
        return opponentHand;
    }

    public void setOpponentHand(String opponentHand) {
        this.opponentHand = opponentHand;
    }

    public String getOutcome() {
        return outcome;
    }

    public void setOutcome(String outcome) {
        this.outcome = outcome;
    }
    
    
}
